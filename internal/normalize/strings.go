package normalize

import (
	"golang.org/x/net/idna"
	"golang.org/x/text/secure/precis"
)

func Domain(s string) (string, error) {
	return idna.Lookup.ToUnicode(s)
}

func User(s string) (string, error) {
	return precis.UsernameCaseMapped.CompareKey(s)
}
